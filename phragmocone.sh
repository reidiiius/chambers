erroreport () {
  printf "\n\n\t%s\n" "Error: null string argument '$1'" >&2
}

# fingerboard

BEADGCF () {
  if [ -n "$1" ]
    then
    pegbox_Fn "$1"
    pegbox_Cn "$1"
    pegbox_Gn "$1"
    pegbox_Dn "$1"
    pegbox_An "$1"
    pegbox_En "$1"
    pegbox_Bn "$1"
  else
    erroreport "$1"
  fi
}

